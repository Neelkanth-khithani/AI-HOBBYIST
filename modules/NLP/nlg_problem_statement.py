def generate_problem_statement(chosen_words, task):

    problem_statement = (
        f"To {task}, focusing on {', '.join(chosen_words)}, we must address the pressing need for sustainable solutions. "
        f"Challenges such as environmental degradation, resource depletion, and societal impacts require innovative and collaborative approaches. "
        f"Emphasizing long-term environmental, economic, and social stability is essential for fostering a balanced and resilient future. "
        f"By tackling these interconnected issues, we can contribute to a more sustainable and equitable world."
    )
    
    return problem_statement